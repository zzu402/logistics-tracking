package com.htkfood.entity.vo;

import org.springframework.beans.factory.annotation.Value;
import org.springframework.stereotype.Component;

@Component
public class SmsParamsVo {
	@Value("${sms.uid}")
	private String uid;
	@Value("${sms.key}")
	private String key;
	
	@Value("${tx.sms.appId}")
	private String appId;
	@Value("${tx.sms.appKey}")
	private String appKey;
	@Value("${tx.sms.sign}")
	private String sign;
	@Value("${tx.sms.driver.templeteId}")
	private String driverTempleteId;
	@Value("${tx.sms.client.templeteId}")
	private String clientTempleteId;
	@Value("${tx.sms.driver.new.templeteId}")
	private String driverNewTempleteId;
	@Value("${tx.sms.employee.templeteId}")
	private String employeeTempleteId;
	@Value("${tx.sms.enable}")
	private Boolean enable; 
	
	
	
	public String getDriverNewTempleteId() {
		return driverNewTempleteId;
	}
	public void setDriverNewTempleteId(String driverNewTempleteId) {
		this.driverNewTempleteId = driverNewTempleteId;
	}
	public Boolean getEnable() {
		return enable;
	}
	public void setEnable(Boolean enable) {
		this.enable = enable;
	}
	public String getUid() {
		return uid;
	}
	public void setUid(String uid) {
		this.uid = uid;
	}
	public String getKey() {
		return key;
	}
	public void setKey(String key) {
		this.key = key;
	}
	public String getAppId() {
		return appId;
	}
	public void setAppId(String appId) {
		this.appId = appId;
	}
	public String getAppKey() {
		return appKey;
	}
	public void setAppKey(String appKey) {
		this.appKey = appKey;
	}
	public String getSign() {
		return sign;
	}
	public void setSign(String sign) {
		this.sign = sign;
	}
	public String getDriverTempleteId() {
		return driverTempleteId;
	}
	public void setDriverTempleteId(String driverTempleteId) {
		this.driverTempleteId = driverTempleteId;
	}
	public String getClientTempleteId() {
		return clientTempleteId;
	}
	public void setClientTempleteId(String clientTempleteId) {
		this.clientTempleteId = clientTempleteId;
	}
	public String getEmployeeTempleteId() {
		return employeeTempleteId;
	}
	public void setEmployeeTempleteId(String employeeTempleteId) {
		this.employeeTempleteId = employeeTempleteId;
	}
	@Override
	public String toString() {
		return "SmsParamsVo [uid=" + uid + ", key=" + key + ", appId=" + appId + ", appKey=" + appKey + ", sign=" + sign
				+ ", driverTempleteId=" + driverTempleteId + ", clientTempleteId=" + clientTempleteId
				+ ", employeeTempleteId=" + employeeTempleteId + "]";
	}
	
	
}
