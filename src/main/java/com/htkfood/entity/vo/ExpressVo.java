package com.htkfood.entity.vo;

import java.util.List;

import com.htkfood.entity.ExpressEntity;

/*
 * 封装前台传过来的出库单信息
 */
public class ExpressVo {
	private String logisticsNo;
	private List<ExpressEntity> expressEntities;
	public String getLogisticsNo() {
		return logisticsNo;
	}
	public void setLogisticsNo(String logisticsNo) {
		this.logisticsNo = logisticsNo;
	}
	public List<ExpressEntity> getExpressEntities() {
		return expressEntities;
	}
	public void setExpressEntities(List<ExpressEntity> expressEntities) {
		this.expressEntities = expressEntities;
	}
	@Override
	public String toString() {
		return "ExpressList [logisticsNo=" + logisticsNo + ", expressEntities=" + expressEntities + "]";
	}
	
	

}
